public class BinaryTreeDiameter<H, D> {
    private H height;
    private D diameter;

    public H getHeight() {
        return height;
    }

    public void setHeight(H height) {
        this.height = height;
    }

    public D getDiameter() {
        return diameter;
    }

    public void setDiameter(D diameter) {
        this.diameter = diameter;
    }
}


